export function getPreviewStepTitle(stepNumber, language, fallbackTitle) {
  const map = PREVIEW_STEP_TITLES[language];
  if (map && map[stepNumber]) return map[stepNumber];
  return fallbackTitle || '';
}

export function getPreviewFieldLabel(fieldKey, language, formatFieldLabelFallback) {
  if (!fieldKey) return '';
  const pack = PREVIEW_FIELD_LABELS[language];
  if (pack && pack[fieldKey]) return pack[fieldKey];
  return formatFieldLabelFallback(fieldKey);
}

export function getDeliveryLineLabels(language) {
  return PREVIEW_DELIVERY_LINE_LABELS[language] || DEFAULT_DELIVERY_LINE_LABELS;
}
